    public void searchMed(String name){
        try{
            Connection con= JConnection.getCon();
            Statement st= con.createStatement();
            ResultSet rs= st.executeQuery("select * from medicine where medName like '"+name+"%'");
            if(rs.next()){
                Stf.setText(rs.getString(1));
                Tf1.setText(rs.getString(2));
                rsD1.setDatoFecha(rs.getDate(4));
                rsD2.setDatoFecha(rs.getDate(5));
                Tf2.setText(rs.getString(3));
                Tf4.setText(rs.getString(7));
                Tf3.setText(rs.getString(6));
                Stf.setEditable(false);
            }
        } catch(Exception e){
        }
    }
    
    public String date2str(java.util.Date sdate){
        SimpleDateFormat sdf= new SimpleDateFormat("yyyyMMdd");
        return sdf.format(sdate);
    }

