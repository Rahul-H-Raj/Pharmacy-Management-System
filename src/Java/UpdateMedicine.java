    private void UbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UbActionPerformed
        // TODO add your handling code here:
        int a= Integer.parseInt(adq.getText());
        int q= Integer.parseInt(Tf4.getText());
        int price= Integer.parseInt(Tf3.getText());
        java.util.Date mfgDate= rsD1.getDatoFecha();
        java.util.Date expDate= rsD2.getDatoFecha();
        Date today= new Date();
        String md= date2str(mfgDate);
        String ed= date2str(expDate);
        String td= date2str(today);
        int m= Integer.parseInt(md);
        int e= Integer.parseInt(ed);
        int t= Integer.parseInt(td);
        int p= a + q;
        try{
            if((a > 100)||(p > 200)){
                JOptionPane.showMessageDialog(null,"Entered Additional quantity exceeds the LIMIT!","Message",JOptionPane.ERROR_MESSAGE);
                adq.setText("");
            }
            else if(price <= 0){
                JOptionPane.showMessageDialog(null,"Price cannot be Negative!","INVALID!",JOptionPane.ERROR_MESSAGE);
                Tf3.setText("");
            }
            else if((mfgDate.compareTo(expDate)>=0)||(m >= t)){
                JOptionPane.showMessageDialog(null,"Please Enter the Valid Date for the 'Manufacture Date'!","INVALID!",JOptionPane.ERROR_MESSAGE);
            }
            else if(((e-m) <= 230)||((e-m) >= 10000)){
                JOptionPane.showMessageDialog(null,"Please Enter the Valid Date for the 'Expiry Date'!","INVALID!",JOptionPane.ERROR_MESSAGE);
            }
            else{
                String qty= Integer.toString(p);
                Tf4.setText(qty);
                adq.setText("");
                updateMed();
            }
        } catch(Exception ex){

        }
    }//GEN-LAST:event_UbActionPerformed

