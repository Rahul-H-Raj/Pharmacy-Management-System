    public void addMedData(){
        int mId= Integer.parseInt(idl1.getText());
        String mName= Tf1.getText();
        int cId= Integer.parseInt(jcid.getText());
        int price= Integer.parseInt(Tf3.getText());
        int quantity= Integer.parseInt(Tf4.getText());
        String validity, status;
        
        java.util.Date mfgDate= rsD1.getDatoFecha();
        java.util.Date expDate= rsD2.getDatoFecha();
        java.sql.Date sMfgD= new java.sql.Date(mfgDate.getTime());
        java.sql.Date sExpD= new java.sql.Date(expDate.getTime());
        Date today= new Date();
        
        String mfg_Date= date2str(sMfgD);
        String exp_Date= date2str(sExpD);
        String td= date2str(today);
        
        int ex= Integer.parseInt(exp_Date);
        int t= Integer.parseInt(td);
        if( ex > t ){
            validity="valid";
            status="active";
        } else{
            validity="expired";
            status="inactive";
        }
        
        try{
            Connection con= JConnection.getCon();
            Statement st= con.createStatement();
            st.executeUpdate("insert into medicine values("+mId+",'"+mName+"',"+cId+","+mfg_Date+","+exp_Date+","+price+","+quantity+",'"+validity+"','"+status+"')");
            JOptionPane.showMessageDialog(null,"Successfully Added!");
            new AddMedicine().setVisible(true);
        } catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
    public String date2str(java.util.Date sdate){
        SimpleDateFormat sdf= new SimpleDateFormat("yyyyMMdd");
        return sdf.format(sdate);
    }
    

