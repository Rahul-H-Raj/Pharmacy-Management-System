    private void jcbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbActionPerformed
        // TODO add your handling code here:
        String cName= Tf2.getText();
        try{
            Connection con= JConnection.getCon();
            Statement st= con.createStatement();
            ResultSet rs= st.executeQuery("select * from company where compName like '"+cName+"%'");
            if(rs.next()){
                Tf2.setForeground(black);
                Tf2.setText(rs.getString(2));
                jcid.setText(rs.getString(1));
            } else{
                int a= JOptionPane.showConfirmDialog(null,"Entered Company's data NOT FOUND!\nDo you want to add New Company' details?","Message",JOptionPane.YES_NO_OPTION);
                if(a==0){
                    new AddCompany().setVisible(true);
                }    
            }
        } catch(Exception e){
        }
    }//GEN-LAST:event_jcbActionPerformed

